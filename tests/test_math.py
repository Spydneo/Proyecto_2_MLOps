import mipaquete.math as math
# from mipaquete.math import sumar

def test_math_sumar():
    """Comprueba que la operación sumar sea correcta
    """
    assert math.sumar(3, 5) == 8

def test_math_restar():
    """Comprueba que la operación restar sea correcta
    """
    assert math.restar(7, 2) == 5

def test_math_multiplicar():
    """Comprueba que la operación multiplicar sea correcta
    """
    assert math.restar(7, 2) == 5